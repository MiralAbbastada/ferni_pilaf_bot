def start_growing(user_id):
    growing_time = random.randint(3500, 3600)
    planting_time = int(time.time())

    cursor.execute("""
        UPDATE users
        SET planting_time = ?, is_growing = ?, rice_quantity = COALESCE(rice_quantity, 0)
        WHERE user_id = ?
    """, (planting_time + growing_time, 1, user_id))

    conn.commit()
    Timer(growing_time, finish_growing, [user_id]).start()

def finish_growing(user_id):
    random_num = random.randint(100, 1000)

    cursor.execute("UPDATE users SET is_growing = 0, rice_quantity = rice_quantity + ? WHERE user_id = ?", (random_num, user_id))
    conn.commit()


